    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Color c = JColorChooser.showDialog(null, "Choose a color", rgb.getColor());
        if (c == null) {
            return;
        }
        rgb.setR((float) c.getRed() / 255);
        rgb.setG((float) c.getGreen() / 255);
        rgb.setB((float) c.getBlue() / 255);
        blocked = true;
        //rgb.updateAll();
        cmyk.updateAll();
        hsv.updateAll();
        LAB.updateAll();
        updateColor();
        blocked = false;
    }//GEN-LAST:event_jButton1ActionPerformed

